    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tb_cat.getModel();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();   
            ResultSet rs = st.executeQuery("select *from client");
            while(rs.next()) {
                model.addRow(new Object[] {rs.getString("client_pk"),rs.getString("name"),rs.getString("mobileNumber"),rs.getString("email")});
            }
            con.close();
            st.close();
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        b_atualizar.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

    private void tb_catMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_catMouseClicked
        // TODO add your handling code here:
        int index = tb_cat.getSelectedRow();
        TableModel model = tb_cat.getModel();
        
        String id = model.getValueAt(index, 0).toString();
        clientPK = Integer.parseInt(id);
        
        String nome = model.getValueAt(index, 1).toString();
        i_nomecliente.setText(nome);
        
        String telefone = model.getValueAt(index, 2).toString();
        i_mobileNumber.setText(telefone);
        
        String email = model.getValueAt(index, 3).toString();
        i_email.setText(email);
        
        b_salvar.setEnabled(false);
        b_atualizar.setEnabled(true);
    }//GEN-LAST:event_tb_catMouseClicked

