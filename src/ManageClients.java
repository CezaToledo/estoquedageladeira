    private void b_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_salvarActionPerformed
        // TODO add your handling code here:
        String nome = i_nomecliente.getText();
        String telefone = i_mobileNumber.getText();
        String email = i_email.getText();
        
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("insert into client (name,mobileNumber,email) values(?,?,?)");
            ps.setString(1,nome);
            ps.setString(2,telefone);
            ps.setString(3,email);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Cliente adicionado com sucesso");
            setVisible(false);
            new ManageClients().setVisible(true);
            con.close();
            ps.close();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_b_salvarActionPerformed

    private void b_atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_atualizarActionPerformed
        // TODO add your handling code here:
        String nome = i_nomecliente.getText();
        String telefone = i_mobileNumber.getText();
        String email = i_email.getText();

        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("update client set name=?,mobileNumber=?,email=? where client_pk=?");
            ps.setString(1, nome);
            ps.setString(2, telefone);
            ps.setString(3, email);
            ps.setInt(4, clientPK);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso");
            setVisible(false);
            new ManageClients().setVisible(true);
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_b_atualizarActionPerformed

